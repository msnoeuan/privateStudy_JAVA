public class Code52 {
    public static void main(String[] args) {
        int score = 80;

        switch(score) {
            case 90 :
                System.out.println("Your score is 90");
                break;
            case 80 :
                System.out.println("Your score is 80");
                break;
            case 70 :
                System.out.println("Your score is 70");
                break;
            default :
                System.out.println("Your score is under 70");
        }
    }
}
